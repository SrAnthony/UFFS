public class Funcionario{
	String nome;
	String cpf;
	int idade;
	float salario;

	public Funcionario(){}
	public Funcionario(String nome, String cpf, int idade, float salario){
		this.nome = nome;
		this.cpf = cpf;
		this.idade = idade;
		this.salario = salario;
	}
}